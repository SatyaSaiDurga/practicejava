public class operators {
    public static void main(String [] args)
    {
        int x=25;
        int y=32;
    //returns false if x value is less than y
    System.out.println(x>y);
    //returns true if x value is than y
    System.out.println(x<y);
    System.out.println(x>=y); 
    System.out.println(x<=y);
    System.out.println(x!=y);
    System.out.println(x==y);
    System.out.println(x>10&&y>10);
    System.out.println(x>10||y>10);
    System.out.println(!(x>10&&y>10));
}
}
