public class exampleelseif {
    public static void main(String [] args)
    {
     int time=9;
     if(time>=6)
     {
        System.out.println("Good Morning");
     }
     else if(time>=12)
     {
        System.out.println("Good Afternoon");
     }
     else
     {
        System.out.println("Good evening");
     }

    }
    
}

